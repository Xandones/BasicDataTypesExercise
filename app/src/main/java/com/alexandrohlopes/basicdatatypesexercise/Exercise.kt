package com.alexandrohlopes.basicdatatypesexercise

fun main() {
    val str = "Android Masterclass"
    val float = 13.37f
    val double = 3.14159265358979
    val short : Short = 25
    val int = 2020
    val long : Long = 18881234567
    val bool = true
    val char = 'a'
    println("String: $str")
    println("Float: $float")
    println("Double: $double")
    println("Short: $short")
    println("Integer (32 bits): $int")
    println("Long: $long")
    println("Boolean: $bool")
    println("Char: $char")
}